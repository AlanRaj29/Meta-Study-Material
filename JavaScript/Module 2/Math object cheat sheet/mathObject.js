console.log(my); //APPROXIMATE OF MATH.IP VALUE IS 3.14159

//2)EULER'S CONSTANT

Math.E;
var my = Math.E;
console.log(my); //APPROXIMATE OF MATH.E VALUE IS 2.718

//3)NATURAL LOGARITHM

Math.LN2;
var my = Math.LN2;
console.log(my); //APPROXIMATE OF MATH.LN2 VALUE IS 0.693

//ROUNDING METHOD

var my = Math.ceil(22.2);
console.log(my); //ROUND UP TO CLOSER INTERGER 23

var my = Math.floor(22.2);
console.log(my); //ROUND DOWN TO CLOSER INTERGER 22

var my = Math.round(22.5);
console.log(my); //ROUNDS UP TO THE CLOSEST INTEGER IF THE DECIMAL IS .5 OR ABOVE; OTHERWISE, ROUNDS DOWN TO THE CLOSEST INTEGER

var my = Math.trunc(22.3);
console.log(my); //TRIMS THE DECIMAL, LEAVING ONLY THE INTEGER

//ARITHMETIC AND CALCULUS METHOD

var my = Math.pow(2, 3); //POWER THE NUMBER,RESULT IS 8
console.log(my);
var my = Math.sqrt(16); //SQUARE ROOT OF 16 IS,4
console.log(my);
var my = Math.cbrt(8); //CUBR ROOT OF 8 IS,2
console.log(my);
var my = Math.abs(-10); //RETURNS THE ABSOLUTE VALUE, THE RESULT IS 10
console.log(my);

//LOGICAL METHOD
var my = Math.log(15)
console.log(my);

var my = Math.log2(15)
console.log(my);

var my = Math.log10(15)
console.log(my);

//MIN AND MAX

var my = Math.min(9,8,7)
console.log(my)//7


var my = Math.max(9,8,7)
console.log(my)//9

